/**
 *
 * @param target {number}
 * @param num {number}
 * @returns {number}
 */
export const increase = (target, num = 1) => {
  return target + num
}

/**
 *
 * @param target {number}
 * @param num {number}
 * @returns {number}
 */
export const decrease = (target, num = 1) => {

  return target - num
}

/**
 *
 * @param a {number}
 * @param b {number}
 * @returns {number}
 */
export const plus = (a, b) => {
  return a + b
}

/**
 *
 * @param a {number}
 * @param b {number}
 * @returns {number}
 */
export const minus = (a, b) => {
  return a - b
}
